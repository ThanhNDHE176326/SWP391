package Models;

/**
 *
 * @author Acer
 */
public class OrderStatus {
    private String id, name , isDelete;

    public OrderStatus() {
    }

    public OrderStatus(String id, String name, String isDelete) {
        this.id = id;
        this.name = name;
        this.isDelete = isDelete;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIsDelete() {
        return isDelete;
    }

    public void setIsDelete(String isDelete) {
        this.isDelete = isDelete;
    }
    
}
