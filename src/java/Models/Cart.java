package Models;

/**
 *
 * @author admin
 */
public class Cart {

    String id, customer;

    public Cart() {
    }

    public Cart(String id, String customer) {
        this.id = id;
        this.customer = customer;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCustomer() {
        return customer;
    }

    public void setCustomer(String customer) {
        this.customer = customer;
    }

}
